/** lens（分解レンズ）= 具体的な子ノード候補プロンプト */
export type IdeationLens = {
  /** レンズの見出し（チェックリストのラベル） */
  label: string;
  /** 子ノード候補として投入される具体プロンプト（編集可能） */
  prompt: string;
};

/** 発想法が示唆する子ノードの既定種別 */
export type IdeationDefaultKind = 'CAUSE' | 'COUNTERMEASURE';

/** 発想法エントリ */
export type IdeationMethod = {
  /** 一意キー */
  key: string;
  /** 発想法の名称 */
  name: string;
  /** 何のための発想法か（目的） */
  purpose: string;
  /**
   * 起動トリガー / 症状（行き詰まりの兆候）。
   * 利用者が「いまどの法を使うべきか」を選びやすくするための一文。
   * json に明示の項目が無いため purpose から抽出して付与。
   */
  trigger: string;
  /** 分解レンズ（= 子ノード候補プロンプト群） */
  lenses: IdeationLens[];
  /** 具体例 */
  example: string;
  /** UI 設計ヒント */
  uiHint: string;
  /**
   * 子ノードの既定種別。打ち手創出系は COUNTERMEASURE、
   * 主体推論など原因/構造分析寄りは CAUSE。利用者が UI でトグル可能。
   */
  defaultKind: IdeationDefaultKind;
};

